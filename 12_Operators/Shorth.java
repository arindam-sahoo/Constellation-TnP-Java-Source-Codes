public class Shorth {
    public static void main(String args[]) {
        int x=5, y=6;
        x += y;
        System.out.println(x);

        x=5;
        y=6;
        x -= y;
        System.out.println(x);

        x=5;
        y=6;
        x *= y;
        System.out.println(x);

        x=5;
        y=6;
        x /= y;
        System.out.println(x);

        x=5;
        y=6;
        x %= y;
        System.out.println(x);
    }
}
