public class RelOper {
    public static void main(String args[]) {
        int x=5, y=6;
        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x>=y);
        System.out.println(x<=y);
        System.out.println(x==y);
        System.out.println(x!=y);
    }
}
