public class ForLoops {
    public static void main(String args[]){
        // Iterating from 1 to 10, using i++
        for(int i=1;i<=10;i++){
            System.out.println(i);
        }

        // Iterating from 1 to 10, using i=i+3
        for(int i=1;i<=10;i=i+3){
            System.out.println(i);
        }

        // Iterating from 10 to 1, using i--
        for(int i=10;i>=1;i--){
            System.out.println(i);
        }

        // Iterating from 10 to 1, using i=i-3
        for(int i=10;i>=1;i=i-3){
            System.out.println(i);
        }
    }
    
}
